# 3) Пару занятий назад мы писали функцию, которая определяет,
# является ли введённое с клавиатуры число простым. Напишите
# программу, использующую эту функцию следующим образом. Программа
# принимает на вход натуральные числа, пока не встретит 0, и записывает
# в список те из вводимых чисел, которые являются простыми. После ввода
# нуля программа выводит список введённых простых чисел в порядке убывания
# Пример ввода
# 5
# 2
# 6
# 13
# 0
# При таких входных данных программа должна вывести
# 13
# 5
# 2

def is_prime(n):
    if n < 2:
        return False
    for i in range(2, int(n**0.5) + 1):
        if n % i == 0:
            return False
    return True


def collect_primes_until_zero():
    primes = []

    while True:
        num = int(input('Введите натуральное число (0 - для завершения): '))
        if num == 0:
            break
        if is_prime(num):
            primes.append(num)

    primes.sort(reverse=True)

    print('Простые числа в порядке убывания:')
    for prime in primes:
        print(prime)


collect_primes_until_zero()
